package com.example.surveyapplication.models;

public class MaritalStatusCount {

    private int unmarried_count;
    private int married_count;
    private int divorce_count;
    private int widow_count;

    public int getUnmarried_count() {
        return unmarried_count;
    }

    public void setUnmarried_count(int unmarried_count) {
        this.unmarried_count = unmarried_count;
    }

    public int getMarried_count() {
        return married_count;
    }

    public void setMarried_count(int married_count) {
        this.married_count = married_count;
    }

    public int getDivorce_count() {
        return divorce_count;
    }

    public void setDivorce_count(int divorce_count) {
        this.divorce_count = divorce_count;
    }

    public int getWidow_count() {
        return widow_count;
    }

    public void setWidow_count(int widow_count) {
        this.widow_count = widow_count;
    }
}
